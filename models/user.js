const mongoose = require("mongoose");

//SCHEMA
const userSchema =new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    gender: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

//MODEL
const User = mongoose.model("User", userSchema);
module.exports = User;